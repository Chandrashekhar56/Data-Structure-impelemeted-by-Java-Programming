public class multiArray {
  public static void main(String[] args) {
    int[][] numbers = { {4, 2, 1, 7},
    					  {8, 2, 5}
                        };
    int x = numbers [0][0];
    int y = numbers [0][1];
    int z = numbers [0][2];
    int m = numbers [0][3];
    int n = numbers [1][0];
    int p = numbers [1][1];
    int q = numbers [1][2];
    System.out.println(x+" "+y+" "+z+" "+m+" "+n+" "+p+" "+q);
  }
}
